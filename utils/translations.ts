export type TranslationKey = keyof typeof translations;

export function t(key: TranslationKey, language: AppLanguage): string {
  const entry = translations[key];
  return entry[language] ?? entry.bg;
}

export function getLanguageLabel(code: AppLanguage): string {
  return LANGUAGE_OPTIONS.find((o) => o.code === code)?.label ?? code;
}

export function getLanguageFlag(code: AppLanguage): string {
  return LANGUAGE_OPTIONS.find((o) => o.code === code)?.flag ?? "";
}

/**
 * Returns the language name in a format suitable for AI prompts.
 * E.g. "Bulgarian", "English", "German", "Spanish", "French".
 */
export function getLanguageForPrompt(code: AppLanguage): string {
  if (code === "bg") return "Bulgarian";
  if (code === "en") return "English";
  if (code === "de") return "German";
  if (code === "es") return "Spanish";
  if (code === "fr") return "French";
  return "Bulgarian";
}

export function getLanguageLocale(code: AppLanguage): string {
  if (code === "bg") return "bg-BG";
  if (code === "en") return "en-US";
  if (code === "de") return "de-DE";
  if (code === "es") return "es-ES";
  if (code === "fr") return "fr-FR";
  return "bg-BG";
}

