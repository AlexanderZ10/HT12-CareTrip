const ALL_LANGUAGES: AppLanguage[] = ["bg", "en", "de", "es", "fr"];

let _reverseLookup: Map<string, TranslationKey> | null = null;

function getReverseLookup(): Map<string, TranslationKey> {
  if (_reverseLookup) return _reverseLookup;
  _reverseLookup = new Map();
  const allKeys = [...ONBOARDING_KEYS, ...TRAVEL_PACE_KEYS, ...STAY_STYLE_KEYS];
  for (const key of allKeys) {
    for (const lang of ALL_LANGUAGES) {
      _reverseLookup.set(t(key, lang), key);
    }
  }
  return _reverseLookup;
}

/**
 * Translates a stored onboarding option value to the current language.
 * The stored value could be in any language — this finds the matching
 * translation key and returns the label in the target language.
 * Falls back to the original value if no match is found.
 */
export function translateOnboardingOption(
  storedValue: string,
  language: AppLanguage
): string {
  const key = getReverseLookup().get(storedValue);
  return key ? t(key, language) : storedValue;
}
